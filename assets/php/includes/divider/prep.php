  <h2 class="title">Remember, veggies taste the best when they're cooked for the right amount of time&mdash;not too crunchy and not too soft. Also, fruits are the easiest to eat when prepared in bite-size and simple ways. </h2>
  <h1 class="titleSmall">  Here are some quick tips for getting your family  <span class="green">to eat more fruits and veggies:</span> </h1>
  <section class="steps">
    <div class="row center">
      <div class="col-lg-4 col-md-4 col-sm-4"><img src="assets/img/prep1__01.png"/>
        <h2>Fast Fruit</h2>
        <p>Wash grapes and place in individual baggies or small containers in the fridge for quick, grab-and-go snacks.	</p>
      </div>
      <div class="col-lg-4 col-md-4 col-sm-4"><img src="assets/img/prep1__03.png"/>
        <h2>	Veggies On-The-Go</h2>
        <p>Cut up a bunch of raw veggies (peppers, carrots, or broccoli) and keep them in the fridge for quick snacks&mdash;or put them out in a bowl when kids come home from school. </p>
      </div>
      <div class="col-lg-4 col-md-4 col-sm-4"><img src="assets/img/prep1__05.png"/>
        <h2>	For The Sweet Tooth</h2>
        <p>Serve fruit (sliced melon or strawberries) for a sweet and healthy dessert.	</p>
      </div>
    </div>
    <div class="row center">
      <div class="col-lg-4 col-md-4 col-sm-4"><img src="assets/img/prep2__01.png"/>
        <h2>Spice It Up</h2>
        <p>Get creative with healthy seasonings&mdash;like basil, thyme, and oregano&mdash;to make veggies tasty without adding salt or fats. </p>
      </div>
      <div class="col-lg-4 col-md-4 col-sm-4"><img src="assets/img/prep2__03.png"/>
        <h2>	Add A Side Of Veggies</h2>
        <p>Steam fresh or frozen vegetables as a quick side dish to your dinner. Just put veggies in a bowl, add a small amount of water, and microwave for a few minutes.		</p>
      </div>
      <div class="col-lg-4 col-md-4 col-sm-4"><img src="assets/img/prep2__05.png"/>
        <h2>	Stir Things Up</h2>
        <p>Use up leftover veggies at the end of each week in a Stir-Fry.	</p>
      </div>
    </div>
  </section>
