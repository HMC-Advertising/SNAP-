  <h2 class="title">As the adult in charge, you play a big role in sending positive messages to your kids about physical activity and eating healthy. When it comes to being a healthy eating role model for your kids, we recommend these simple steps: </h2>
  <section class="row steps">
    <div class="col-lg-4 col-md-4 col-sm-4">
      <header><img src="assets/img/SNAP-ICONS_02.jpeg"/>
        <h3>Try new healthy foods </h3>
      </header>
      <article>Your kids learn their eating habits from watching you. So let them see you eat more fruits and veggies&mdash;and they will too!</article>
    </div>
    <div class="col-lg-4 col-md-4 col-sm-4"> 
      <header><img src="assets/img/SNAP-ICONS_07.png"/>
        <h3>Stay positive</h3>
      </header>
      <article>When talking to your kids about healthy eating, remember to stay positive. Don't talk about dieting or losing weight. Instead, talk about how great exercise and eating well makes you feel.</article>
    </div>
    <div class="col-lg-4 col-md-4 col-sm-4"> 
      <header><img src="assets/img/SNAP-ICONS_04.png"/>
        <h3>Make time for family meals</h3>
      </header>
      <article>Regular family meals are a great opportunity to spend time with your family. Set up regular meal times as much as possible. Make it a happy time by talking to your kids about their day and learning about new foods. </article>
    </div>
  </section>
