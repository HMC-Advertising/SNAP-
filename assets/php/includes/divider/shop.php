  <h2 class="title">Once you know what you're looking for, it's time to plan your trip to the grocery store or a local farmers' market. </h2>
  <h1 class="titleSmall">TIPS FOR MAKING <span class="purple">YOUR SHOPPING LIST</span></h1>
  <section class="row steps">
    <div class="col-lg-6 col-md-6">
      <div class="listContent">
        <header>
          <h2>Start with what you have </h2>
        </header>
        <article>Check your fridge, freezer, and cabinets. Plan meals that use what you already have in the house.</article>
      </div>
      <div class="listContent">
        <header>
          <h2>Think about the fruits and veggies first  </h2>
        </header>
        <article>Meals based around fruits and veggies are cheaper&mdash;and better for you&mdash;than meat. <a href=" http://www.sustainabletable.org/seasonalfoodguide/" target="_blank">Look at what's in season</a>, what looks good, and what is on sale&mdash;and think of how you can base meals around these fruits and veggies.</article>
      </div>
      <div class="listContent">
        <header>
          <h2>Look up budget-friendly recipes</h2>
        </header>
        <article>There are lots of sites with healthy, low-cost recipes. Try <a href="https://www.foodhero.org/recipes/healthy-recipes" target="_blank">Food Hero</a> or <a href="http://www.extension.iastate.edu/foodsavings/recipes/all" target="_blank">Spend Smart Eat Smart</a> for ideas. Get your family in on the fun by asking them to rate new meals on a scale of 1 to 5. When you find a recipe that your family rates a 3 or above, keep it on hand for future meal planning!	</article>
      </div>
    </div>
    <div class="col-lg-6 col-md-6"> 
      <div class="listContent"> 
        <header>
          <h2>Plan for 5-6 meals with extra for leftovers</h2>
        </header>
        <article>Cooking larger batches helps you save money by buying ingredients in larger quantities&mdash;and using the leftovers for lunch.</article>
      </div>
      <div class="listContent">
        <header>
          <h2>KEEP CHEAP AND EASY-TO-COOK STAPLE FOODS TO MAKE MEALTIME SIMPLE </h2>
        </header>
        <article>Buy extras of foods that you can use regularly and that keep on the shelf (called staple foods) when they're on sale, like rice, dry beans, peanut butter, canned soup, and canned or frozen vegetables. Having staples of cheap and easy-to-cook foods can make mealtime simple.</article>
      </div>
    </div>
